    private void varjKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_varjKeyReleased
        if (varj.getText().equals("-")) {
            varj.setBackground(Color.BLUE);
            JOptionPane.showMessageDialog(null, "parabens, passou de fase");
            if (erros == 1) {
                Pessoa.setPontuacao(Pessoa.getPontuacao() + 8);
            } else if (erros == 2) {
                Pessoa.setPontuacao(Pessoa.getPontuacao() + 6);
            } else {
                Pessoa.setPontuacao(Pessoa.getPontuacao() + 10);
            }
            this.dispose();
            Tela7 tela7 = new Tela7();
            tela7.setVisible(true);

        } else {
            varj.setBackground(Color.RED);
            varj.setText(null);
            varj.requestFocus();
            erros++;
            this.volta();
        }
    }//GEN-LAST:event_varjKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       int confirm = JOptionPane.showConfirmDialog((Component) evt.getSource(),
                "Deseja Realmente Desistir?",
                "Sim - Nao",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (confirm == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(null, "Sua Pontuaçao foi de:" + Pessoa.getPontuacao() + "\n" + "Obrigado(a) por participar do nosso jogo!!");
            this.dispose();
            Menu menu = new Menu();
            menu.setVisible(true);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

