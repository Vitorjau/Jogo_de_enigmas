    private void responderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_responderMouseClicked
        if (resposta.getText().equals("ismael")) {
            JOptionPane.showMessageDialog(null, "Resposta Correta!!!");
            if (erros == 1) {
                Pessoa.setPontuacao(Pessoa.getPontuacao() + 8);
            } else if (erros == 2) {
                Pessoa.setPontuacao(Pessoa.getPontuacao() + 6);
            } else {
                Pessoa.setPontuacao(Pessoa.getPontuacao() + 10);
            }
            this.dispose();
            teste fase5 = new teste();
            fase5.setVisible(true);
        } else {
            erros++;
            this.volta();
            JOptionPane.showMessageDialog(null, "Resposta Incorreta, tente Novamente");
        }
    }//GEN-LAST:event_responderMouseClicked

